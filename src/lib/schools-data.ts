/**
 * Search institutions by name, acronym, location, or state.
 * Optionally filter by category and/or type.
 */
export function searchInstitutions(
  query: string,
  options: {
    category?: InstitutionCategory;
    type?: OwnershipType;
    limit?: number;
  } = {},
): Institution[] {
  if (!query || query.length < 2) return [];

  const q = query.toLowerCase().trim();
  const { category, type, limit = 10 } = options;

  return ALL_INSTITUTIONS.filter(inst => {
    const matchesQuery =
      inst.name.toLowerCase().includes(q) ||
      inst.acronym.toLowerCase().includes(q) ||
      inst.location.toLowerCase().includes(q) ||
      inst.state.toLowerCase().includes(q);

    const matchesCategory = !category || inst.category === category;
    const matchesType = !type || inst.type === type;

    return matchesQuery && matchesCategory && matchesType;
  }).slice(0, limit);
}

/** Backwards-compatible university-only search */
export function searchSchools(query: string, limit = 10) {
  return searchInstitutions(query, { category: 'university', limit });
}

/**
 * Get departments / programmes for a specific institution by name or acronym.
 */
// Replace the existing getDepartments function (around line 1800+) with this:

export function getDepartments(nameOrAcronym: string): string[] {
  const n = nameOrAcronym.toLowerCase().trim();
  
  // Step 1: Try exact name match
  let inst = ALL_INSTITUTIONS.find(
    i => i.name.toLowerCase() === n
  );
  
  // Step 2: Try exact acronym match
  if (!inst) {
    inst = ALL_INSTITUTIONS.find(
      i => i.acronym.toLowerCase() === n
    );
  }
  
  // Step 3: Try partial match
  if (!inst) {
    inst = ALL_INSTITUTIONS.find(
      i => i.name.toLowerCase().includes(n) ||
           i.acronym.toLowerCase().includes(n)
    );
  }
  
  return inst?.departments ?? [];
}

